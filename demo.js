function printSteps(steps) {
    steps.forEach((step, i) => {
        const icon = getIcon(step.action);
        console.log(`  ${i + 1}. ${icon} [${step.node}] ${step.action}: ${step.description}`);
    });
}

function getIcon(action) {
    const icons = {
        [Action.VISITED]: 'üëÅÔ∏è',
        [Action.COMPARED]: '‚öñÔ∏è',
        [Action.MOVED_LEFT]: '‚¨ÖÔ∏è',
        [Action.MOVED_RIGHT]: '‚û°Ô∏è',
        [Action.INSERTED]: '‚úÖ',
        [Action.FOUND]: 'üéØ',
        [Action.NOT_FOUND]: '‚ùå'
    };
    return icons[action] || '‚Ä¢';
}
